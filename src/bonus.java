public class bonus {
    public static void main(String[] args) {

        int customerAccount = 100;
        int amountReplenishment = 1100;

        int finalScore;
        int numberBonus;

        if ( amountReplenishment > 1000) {
            System.out.println( numberBonus = amountReplenishment / 100);
            System.out.println( finalScore = customerAccount + amountReplenishment + numberBonus);
        } else {
            System.out.println( numberBonus = 0);
            System.out.println( finalScore = customerAccount + amountReplenishment);
        }
    }
}
